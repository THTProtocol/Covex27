/// Synchronous core: write temp proof json, invoke node <script> <tmp>, parse {valid: bool}.
/// Dedupes the ~8 near-identical copies that existed in oracle.rs.
fn run_groth16_verifier_sync(
    script: &PathBuf,
    tmp_prefix: &str,
    proof: &Value,
    public_inputs: &[String],
) -> Result<bool, String> {
    let proof_json = serde_json::json!({
        "proof": proof,
        "publicSignals": public_inputs,
    });
    let tmp_path =
        std::env::temp_dir().join(format!("{}_{}.json", tmp_prefix, uuid::Uuid::new_v4()));
    std::fs::write(
        &tmp_path,
        serde_json::to_string(&proof_json).map_err(|e| e.to_string())?,
    )
    .map_err(|e| format!("Failed to write temp proof: {}", e))?;

    // Spawn with a wall-clock timeout so a hung or malicious verifier can never hold a
    // spawn_blocking thread forever (a self-inflicted DoS). On timeout we kill the child and
    // fail closed: the oracle refuses to sign when verification does not complete in time.
    const VERIFY_TIMEOUT: std::time::Duration = std::time::Duration::from_secs(20);
    let mut child = Command::new(node_binary())
        .arg(script.to_str().unwrap_or("zk/verify.js"))
        .arg(&tmp_path)
        .stdout(std::process::Stdio::piped())
        .stderr(std::process::Stdio::piped())
        .spawn()
        .map_err(|e| {
            format!(
                "Failed to run verifier (node={} script={:?}): {}",
                node_binary(),
                script,
                e
            )
        })?;

    let started = std::time::Instant::now();
    let status = loop {
        match child.try_wait() {
            Ok(Some(st)) => break st,
            Ok(None) => {
                if started.elapsed() >= VERIFY_TIMEOUT {
                    let _ = child.kill();
                    let _ = child.wait();
                    let _ = std::fs::remove_file(&tmp_path);
                    return Err(format!(
                        "Verifier timed out after {}s and was killed",
                        VERIFY_TIMEOUT.as_secs()
                    ));
                }
                std::thread::sleep(std::time::Duration::from_millis(40));
            }
            Err(e) => {
                let _ = std::fs::remove_file(&tmp_path);
                return Err(format!("Verifier wait failed: {}", e));
            }
        }
    };

    let mut stdout = String::new();
    let mut stderr = String::new();
    {
        use std::io::Read as _;
        if let Some(mut o) = child.stdout.take() {
            let _ = o.read_to_string(&mut stdout);
        }
        if let Some(mut e) = child.stderr.take() {
            let _ = e.read_to_string(&mut stderr);
        }
    }
    let _ = std::fs::remove_file(&tmp_path);

    if !status.success() {
        return Err(format!(
            "Verifier exited {}: stdout={} stderr={}",
            status,
            stdout.trim(),
            stderr.trim()
        ));
    }

    if stdout.trim().is_empty() {
        return Err(format!(
            "Verifier produced no output. stderr: {}",
            stderr.trim()
        ));
    }

    let result: Value = serde_json::from_str(&stdout)
        .map_err(|e| format!("Invalid verifier output '{}': {}", stdout.trim(), e))?;

    match result["valid"].as_bool() {
        Some(v) => Ok(v),
        None => Err(format!(
            "Unexpected verifier output: {}",
            serde_json::to_string(&result).unwrap_or_default()
        )),
    }
}

/// Async wrapper (spawn_blocking) for Groth16/snarkjs verifiers.
pub(crate) async fn run_groth16_verifier_async(
    script_rel: &str,
    tmp_prefix: &'static str,
    proof: Value,
    public_inputs: Vec<String>,
) -> Result<bool, String> {
    // Cap concurrent verifications so a burst of proof submissions cannot spawn an unbounded
    // number of node child processes (each holds a blocking thread + CPU). Excess requests wait.
    static VERIFY_SEM: std::sync::OnceLock<tokio::sync::Semaphore> = std::sync::OnceLock::new();
    let _permit = VERIFY_SEM
        .get_or_init(|| tokio::sync::Semaphore::new(6))
        .acquire()
        .await
        .map_err(|e| format!("verify semaphore closed: {}", e))?;
    let script = zk_script_path(script_rel);
    tokio::task::spawn_blocking(move || {
        run_groth16_verifier_sync(&script, tmp_prefix, &proof, &public_inputs)
    })
    .await
    .map_err(|e| format!("Spawn blocking failed: {}", e))?
}

/// Hybrid: if the proof contains a real Groth16 body, run verifier; else accept (attested path).
pub(crate) async fn run_hybrid_groth16_async(
    script_rel: &str,
    prefix: &'static str,
    proof: Value,
    public_inputs: Vec<String>,
) -> Result<bool, String> {
    if proof_has_groth16_body(&proof) {
        run_groth16_verifier_async(script_rel, prefix, proof, public_inputs).await
    } else {
        // FAIL CLOSED. A Hybrid circuit with NO proof body is not cryptographically
        // verified. The old Ok(true) soft-pass let a bodyless request through, which
        // the on-chain oracle payout would trust as "verified". Reject it - a real
        // Groth16 body or a server-resolved game outcome is required to sign.
        Err(format!(
            "hybrid circuit (prefix '{}') requires a Groth16 proof body; bodyless attestation is rejected",
            prefix
        ))
    }
}

/// Find risc0 binary (cli installed via rzup/cargo, or in common paths).
/// Returns Some(cmd) that can be invoked; caller decides args.
fn find_risc0_binary() -> Option<String> {
    let candidates = [
        "/usr/bin/risc0",
        "/usr/local/bin/risc0",
        "/root/.cargo/bin/risc0",
        "/home/*/.cargo/bin/risc0", // glob not expanded here
    ];
    for c in &candidates {
        if std::path::Path::new(c).exists() {
            return Some(c.to_string());
        }
    }
    // Try PATH via Command (cheap probe)
    if let Ok(out) = Command::new("risc0").arg("--version").output() {
        if out.status.success() {
            return Some("risc0".to_string());
        }
    }
    if let Ok(out) = Command::new("cargo").args(["risc0", "--version"]).output() {
        if out.status.success() {
            return Some("cargo".to_string());
        }
    }
    None
}

/// RISC0 stub verifier (vision Phase 0 + §4.6).
/// - If binary available: attempt a verify invocation (illustrative args; real receipt format TBD).
/// - Else (or on failure): accept. This lets "risc0_*" and "verifiable" circuits work immediately
///   as oracle-attested / hybrid until full guest receipts + verifier integration.
pub(crate) async fn verify_risc0_stub(
    guest: &str,
    _proof: Value,
    _public_inputs: Vec<String>,
    _requested_outcome: Option<u32>,
) -> Result<bool, String> {
    // Fail closed by default. There is NO real RISC0 receipt verifier implemented
    // here - the paths below only probe for a binary/receipt FILE, they never
    // cryptographically verify a receipt. Rubber-stamping a "verifiable compute"
    // outcome as proven would be dishonest, so by default we REJECT. An operator can
    // opt back into the permissive dev/testnet behavior with
    // COVEX_ALLOW_STUB_VERIFIERS=true. Mainnet must never set that flag.
    if std::env::var("COVEX_ALLOW_STUB_VERIFIERS").as_deref() != Ok("true") {
        return Err(format!(
            "RISC0 verifier for guest '{}' is not implemented; refusing to attest (set COVEX_ALLOW_STUB_VERIFIERS=true to allow attested stubs on testnet)",
            guest
        ));
    }
    let bin_opt = find_risc0_binary();
    // Sprint 2/3: Support dev guests by loading their _proof.json as receipt.
    // This makes risc0_chess_eval, risc0_poker_solver, financial_formula, etc. "use" the existing artifacts
    // instead of pure always-accept. For "real" RISC0, when risc0 binary present, we exec the guest
    // with the receipt (basic support added here for compatibility + real path).
    let guest_receipt = format!("../zk/risc0_guests/{}_proof.json", guest);
    if std::path::Path::new(&guest_receipt).exists() {
        let bin_opt = find_risc0_binary();
        if bin_opt.is_some() {
            // Real risc0 path: in full impl we'd call the binary with receipt and check output.
            // For now, if binary present and receipt exists, treat as "real verified" for dev.
            return Ok(true);
        }
        return Ok(true);
    }

    if bin_opt.is_none() {
        // No risc0 yet - stub accepts (common for dev, attested compute, or when offloading to external prover).
        return Ok(true);
    }
    let bin = bin_opt.unwrap();
    let guest = guest.to_string(); // own it so it can be moved into 'static spawn_blocking closure

    // Run in blocking task. We don't yet have standardized receipt layout in the OracleVerifyInput,
    // so this is a no-op probe for now. Future: serialize _proof as receipt.json, feed --receipt,
    // parse exit/output for validity.
    let success = tokio::task::spawn_blocking(move || {
        let mut cmd = if bin == "cargo" {
            let mut c = Command::new("cargo");
            c.args(["risc0", "verify", "--guest", &guest]);
            c
        } else {
            let mut c = Command::new(&bin);
            c.arg("verify").arg("--guest").arg(&guest);
            c
        };
        // In real use: .arg("--receipt").arg(tmp) etc.
        // For stub we fire-and-forget and accept.
        let _ = cmd.output();
        true
    })
    .await
    // Fail closed on a JoinError (the verification task panicked): a panicked verification must
    // NOT read as "verified". This is defense-in-depth only: Risc0Stub is excluded from
    // circuit_requires_crypto_proof, so a true verdict here can never mint a money signature on
    // its own (the oracle gate refuses to sign for non-crypto circuits off a caller outcome).
    .unwrap_or(false);

    Ok(success)
}

