/// Primary entrypoint for pluggable verification.
/// Called by oracle handler (after special pre-checks like privacy nullifier).
/// Returns Ok(true) for attested paths (caller still requires requested_outcome for pure attested
/// in the legacy "other" path; we keep that policy in oracle.rs for now).
pub(crate) async fn verify_proof_for_circuit(
    circuit_type: &str,
    proof: Value,
    public_inputs: Vec<String>,
    requested_outcome: Option<u32>,
) -> Result<bool, String> {
    let reg = get_registry();
    match reg.get(circuit_type) {
        Some(VerifierSpec::StrictGroth16 { script, prefix }) => {
            run_groth16_verifier_async(script, prefix, proof, public_inputs).await
        }
        Some(VerifierSpec::HybridGroth16 { script, prefix }) => {
            run_hybrid_groth16_async(script, prefix, proof, public_inputs).await
        }
        Some(VerifierSpec::Risc0Stub { guest }) => {
            // Sprint 1 improvement: Attempt real RISC0 execution if a guest binary is present
            // (e.g. risc0 build output). Falls back to stub for dev.
            verify_risc0_stub(guest, proof, public_inputs, requested_outcome).await
        }
        Some(VerifierSpec::WasmStub { module_hint }) => {
            verify_wasm_exec_stub(module_hint, proof, public_inputs, requested_outcome).await
        }
        Some(VerifierSpec::Attested) => {
            // Deliberate oracle-attested model (games, data feeds, property proofs):
            // verification "succeeds" and the outcome is driven by requested_outcome
            // (enforced in the oracle handler). This is an intentional, registered path.
            Ok(true)
        }
        Some(VerifierSpec::SourceOnlyNoZkey { reason }) => {
            // FAIL CLOSED. The circuit has source + a verify script + a served vkey, but no
            // served proving key, so no honest user can produce a proof. Refuse rather than
            // attest (this is NOT a caller-chosen-outcome path): the claim is not available
            // until a trusted setup ships the served _final.zkey.
            Err(format!(
                "circuit '{}' is not claimable yet: {}",
                circuit_type, reason
            ))
        }
        None => {
            // Fail closed: an UNKNOWN circuit_type has no registered verifier, so we
            // must NOT silently attest it. Returning Err makes the caller
            // (verify_and_sign_handler, which does .unwrap_or(false)) reject the request.
            Err(format!(
                "Unknown circuit_type '{}': no verifier registered (rejected)",
                circuit_type
            ))
        }
    }
}

/// Dynamic outcome determination (replaces the giant else-if chain).
///
/// FAIL-CLOSED INTRINSIC (GAP 12): for a circuit backed by a real cryptographic verifier
/// (Strict/Hybrid Groth16) the outcome is ALWAYS derived from the COMPUTED validity public signal
/// and the caller-supplied `requested_outcome` is IGNORED. Previously this function returned
/// `requested_outcome` verbatim for any circuit, and the fail-closed property lived ONLY in the
/// caller (oracle.rs) remembering to pass `None` on the verified-proof path. That made it one
/// careless future caller away from a free-outcome hole (pass `Some(0)` for a Strict circuit whose
/// proof carries valid==0 and the pot drains). Reading the signal here, unconditionally for
/// Strict/Hybrid, makes the gate intrinsic to the function: no caller can reintroduce the hole.
///
/// Game-attested behavior is UNCHANGED: game pots (chess/tictactoe/connect4) resolve their outcome
/// from the SERVER-recorded result in the oracle handlers (oracle.rs game_row / covenant_builder
/// game_pot_outcome) and never reach the `requested_outcome` branch below for their payout. The
/// tictactoe/connect4 winner is still read from publicSignals[4] in the signal-derivation section.
///
/// For circuits with NO registered crypto verifier (attested / unknown), `requested_outcome` is
/// still honored when supplied (the attested model is gated upstream by circuit_requires_crypto_proof
/// in both oracle handlers, which refuse to sign an attested non-game payout at all).
pub(crate) fn determine_outcome_for_circuit(
    circuit_type: &str,
    proof: &Value,
    public_inputs: &[String],
    requested_outcome: Option<u32>,
) -> u32 {
    // Use registry to decide whether the circuit's outcome is a COMPUTED validity signal. This
    // covers the crypto-proof circuits (Strict/Hybrid) and the SourceOnlyNoZkey DeFi circuits: the
    // latter never reach here in production (verify_proof_for_circuit fails closed before signing),
    // but we keep the same fail-closed signal-reading so a valid==0 proof can never yield outcome 0
    // if they are ever promoted back or reached defensively.
    let reg = get_registry();
    let spec = reg.get(circuit_type);
    let reads_validity_signal = matches!(
        spec,
        Some(VerifierSpec::StrictGroth16 { .. })
            | Some(VerifierSpec::HybridGroth16 { .. })
            | Some(VerifierSpec::SourceOnlyNoZkey { .. })
    );

    // requested_outcome is honored ONLY for circuits that do NOT read a validity signal (attested /
    // unknown). For Strict/Hybrid the requested value is IGNORED and we fall through to the
    // unconditional signal-reading below, so a future caller passing Some(_) cannot mint a free
    // outcome. tictactoe/connect4/privacy_mixer are Strict/Hybrid, so their requested-outcome clamp
    // is gone (their production outcome is the server game result or the verified signal, not a
    // caller value).
    if !reads_validity_signal {
        if let Some(req) = requested_outcome {
            return req;
        }
        return 0;
    }

    // Derivations for the wired full-zk / hybrid circuits (exact same logic as legacy).
    if circuit_type == "merkle_membership" || circuit_type.contains("merkle_") {
        if !public_inputs.is_empty() && public_inputs[0] == "1" {
            0
        } else {
            1
        }
    } else if circuit_type.contains("range") {
        if public_inputs.first().map(|s| s.as_str()) == Some("1") {
            0
        } else {
            1
        }
    } else if matches!(
        circuit_type,
        "age_verification" | "age_verify_v1" | "escrow_2party" | "hash_preimage"
            | "timelock_absolute" | "timelock_abs" | "relative_timelock"
            | "turn_timer" | "pot_split_math" | "vrf_random" | "script_constraint"
            | "utxo_ownership" | "basic_utxo_ownership"
            // The 5 circuits added 2026-06-18 also EXPOSE valid at publicSignals[0]. The three
            // predicate circuits (balance_threshold, solvency_sum, set_non_membership) MUST read
            // it (a below-threshold / not-bracketed proof carries valid==0 and must FAIL the
            // gate); commitment_open + anon_membership_nullifier emit valid==1 by construction,
            // so reading [0]==1 is correct and harmless. Without this they fell through to the
            // proof-presence branch below and a valid==0 proof drained the pot.
            | "commitment_open" | "balance_threshold" | "solvency_sum"
            | "set_non_membership" | "anon_membership_nullifier"
            // New 2026-06-28 primitives also EXPOSE the computed validity at publicSignals[0]:
            // merkle_range_membership = member * (value>=lo) * (value<=hi);
            // equality_of_commitments = (A opens to value) * (B opens to same value). A verifying
            // proof can carry valid==0 (out-of-band value / mismatched commitment), which MUST
            // fail the gate, so we read [0] rather than treating proof presence as success.
            | "merkle_range_membership" | "equality_of_commitments"
            // circomwave 2026-06-28 primitives, valid at publicSignals[0]:
            // nullifier_uniqueness = fresh nullifier bracketed in the spent-set;
            // threshold_sig_knowledge = held shares reconstruct the committed group secret;
            // pedersen_open_equals = committed value == public expected.
            // (merkle_multi_membership + sorted_merkle_range contain "merkle_" so they already hit
            // the merkle branch above, which reads [0] with identical semantics; listed here in a
            // comment for completeness.) A verifying proof can carry valid==0 (spent nullifier,
            // mismatched shares, value != expected), which MUST fail the gate, so we read [0].
            | "nullifier_uniqueness" | "threshold_sig_knowledge" | "pedersen_open_equals"
    ) || circuit_type.contains("timelock")
    {
        // These circuits EXPOSE their gating result as publicSignals[0]
        // (valid / on_time / ok), which is a COMPUTED output of the circuit, not a free
        // input. A verifying proof can legitimately carry [0] = 0 (e.g. age < threshold,
        // timeout not elapsed, move late), so we MUST read it: claimant wins (outcome 0)
        // ONLY when [0] == "1", otherwise the claim fails (outcome 1). This closes the
        // bug where the generic proof_has_groth16_body => 0 fallback below treated mere
        // proof presence as success and let a valid-but-false proof (e.g. an underage
        // age proof) pass the gate. Circuits with no separate valid output (nullifier_set,
        // vrf_dice_roll) intentionally fall through to the proof-presence branch.
        if public_inputs.first().map(|s| s.as_str()) == Some("1") {
            0
        } else {
            1
        }
    } else if matches!(
        circuit_type,
        "collateral_ltv" | "loan_health" | "financial_formula" | "auction_clearing"
    ) {
        // These DeFi/market circuits emit [computed_value, valid] - the gating result
        // is the SECOND public signal (publicSignals[1]), not the first. A real, verifying
        // proof can legitimately carry valid == "0" (e.g. an under-collateralized LTV, an
        // unhealthy loan, a formula that evaluates false, an auction that did not clear),
        // and that case MUST fail the gate. Reading index [1]: claimant wins (outcome 0)
        // ONLY when the validity signal equals "1", otherwise the claim fails (outcome 1).
        // Before this, they fell through to the proof_has_groth16_body => 0 fallback below,
        // so MERE proof presence let a valid-but-false proof drain the pot.
        if public_inputs.get(1).map(|s| s.as_str()) == Some("1") {
            0
        } else {
            1
        }
    } else if proof_has_groth16_body(proof) {
        0
    } else if circuit_type == "tictactoe_v1" || circuit_type == "connect4_v1" {
        if proof_has_groth16_body(proof) && public_inputs.len() >= 5 {
            match public_inputs[4].as_str() {
                "1" => 0,
                "2" => 1,
                "3" => 2,
                _ => 2,
            }
        } else {
            0
        }
    } else if circuit_type.contains("timelock") {
        if public_inputs.first().map(|s| s.as_str()) == Some("1") {
            0
        } else {
            1
        }
    } else if circuit_type == "hash_preimage" {
        if public_inputs.first().map(|s| s.as_str()) == Some("1") {
            0
        } else {
            1
        }
    } else {
        0
    }
}

