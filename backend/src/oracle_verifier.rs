/// Supported verifier kinds in the pluggable registry.
/// Easy to extend for SP1, Halo2, etc. in future phases.
#[derive(Clone, Debug, PartialEq, Eq)]
pub(crate) enum VerifierSpec {
    /// Real Groth16 via snarkjs; always run the verifier script (merkle, range, timelock, hash).
    StrictGroth16 {
        script: &'static str,
        prefix: &'static str,
    },
    /// Groth16 when proof body present (pi_a etc), else treat as oracle-attested success.
    /// Used for the game hybrids tictactoe_v1, connect4_v1.
    HybridGroth16 {
        script: &'static str,
        prefix: &'static str,
    },
    /// RISC0 receipt verification. If risc0 binary (or cargo risc0) found, attempt invoke
    /// (stubbed; real would feed receipt json); otherwise accept (dev + attested fallback).
    Risc0Stub { guest: &'static str },
    /// WASM execution / predicate proof. Stub: runtime if present (wasmtime/node), else accept.
    WasmStub { module_hint: &'static str },
    /// Pure oracle attestation / hybrid property proof (off-chain engine + requested_outcome).
    /// This is the path for the overwhelming majority of the expanded stack.
    Attested,
    /// HONEST "not claimable yet": the circom source, the verify script and the served vkey
    /// exist, but there is NO served `_final.zkey`, so an in-browser prover can never generate a
    /// Groth16 proof for it. Such a circuit must NOT be presented as Groth16-claimable (that
    /// would advertise a ZK claim that always fails closed) and must NOT be attested-with-a-
    /// caller-chosen-outcome (that would sign an unverified outcome). circuit_requires_crypto_proof
    /// returns false for this kind, and verify_proof_for_circuit fails closed with a clear reason.
    /// Promote to Strict/Hybrid only once a trusted setup ships the served proving key.
    SourceOnlyNoZkey { reason: &'static str },
}

/// Returns the static registry map: circuit_type -> verifier spec.
/// Populated once. Adding support for new vision circuits (e.g. utxo_*, vrf_*, defi_*,
/// compute_*, price_*, more primitives) is a one-line insert here. No match arms to touch.
pub(crate) fn get_registry() -> &'static HashMap<&'static str, VerifierSpec> {
    static REG: OnceLock<HashMap<&'static str, VerifierSpec>> = OnceLock::new();
    REG.get_or_init(build_registry)
}

