pub(crate) fn circuit_requires_crypto_proof(circuit_type: &str) -> bool {
    matches!(
        get_registry().get(circuit_type),
        Some(VerifierSpec::StrictGroth16 { .. }) | Some(VerifierSpec::HybridGroth16 { .. })
    )
}

