//! # covex-games-onchain: RISC0->Groth16 receipt -> Kaspa KIP-16 `OpZkPrecompile` (tag 0x20) witness
//!
//! Stage 2 of `docs/ZK_ONCHAIN_PLAN.md`. Once the host produces a real RISC0->Groth16 receipt
//! (`ProverOpts::groth16()`), the settlement covenant must verify it ON-CHAIN via the Kaspa
//! `OpZkPrecompile` opcode (`0xa6`), tag `0x20` (BN254 Groth16). The opcode is a GENERIC
//! ark-groth16 verifier: it pops a verifying key, a proof, an input count, and N field elements,
//! then runs `verify_proof_with_prepared_inputs`. It is NOT RISC0-aware. So the binding to "this
//! exact guest image + this exact journal" has to live in the GROTH16 PUBLIC INPUTS we feed it.
//!
//! ## The RISC0-Groth16 public-input schema (the load-bearing fact)
//!
//! A RISC0 Groth16 receipt verifies against EXACTLY FIVE BN254 `Fr` public inputs, in this order
//! (confirmed against `risc0_groth16::Verifier::new`, risc0-groth16 3.0.4):
//!
//! ```text
//!   [ a0, a1, c0, c1, id_bn254_fr ]
//!     a0,a1        = split_digest(control_root)        // RISC0 recursion control root (version const)
//!     c0,c1        = split_digest(claim_digest)        // SHA-256 digest of the ReceiptClaim
//!     id_bn254_fr  = fr(reverse_bytes(bn254_control_id))// BN254/Poseidon control id (version const)
//! ```
//!
//! `split_digest(d)` reverses `d` to big-endian, splits the 32 bytes in half, and turns each
//! 16-byte half into an `Fr` (so each of a0/a1/c0/c1 is a 128-bit value). The IMAGE ID and the
//! JOURNAL are NOT direct public inputs: they are folded into `claim_digest`. The RISC0
//! `ReceiptClaim` is `{ pre: image_id, post: {pc:0,...}, exit_code: Halted(0), input: ZERO,
//! output: { journal_digest, assumptions: ZERO } }`; its SHA-256 digest is `claim_digest`, and
//! `c0||c1` are that digest split. So:
//!   - a spender CANNOT change the guest program: a different image id changes `pre`, changes the
//!     claim digest, changes c0/c1, and the pinned VK no longer verifies (control root + the
//!     groth16 relation bind image -> claim).
//!   - a spender CANNOT change the journal (winner_pubkey / covenant_id / stake): the journal
//!     digest is inside `output`, inside the claim, inside c0/c1.
//!
//! ## Covenant binding (Stage 3 will enforce this)
//!
//! Because the precompile takes the VK + inputs OFF THE STACK, the SETTLEMENT SCRIPT must bake the
//! VK, a0, a1, and id_bn254_fr as exact-data pushes (a spender cannot swap them), and must bind
//! c0/c1 to the specific covenant. The honest binding to "this pot, this winner" is therefore: the
//! covenant pins `winner_pubkey`/`covenant_id` by REQUIRING the claim digest (c0/c1) that
//! corresponds to a journal with those exact fields. Stage 3 fixes the precise script; this crate
//! just emits the bytes deterministically so Stage 3 + Stage 4 consume identical material.
//!
//! ## What this crate does NOT do
//!
//! It does not itself verify the Groth16 relation (the on-chain node does that), and it does not
//! prove anything (the host does that, needs Docker). It is a pure, deterministic SERIALIZER from a
//! real receipt to on-chain bytes, plus the frozen version constants.

